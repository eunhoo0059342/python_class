# 함수 독립적이다.
def heartbeat_average(lst):
    num = 0 # 합계
    for i in range(len(lst)):
        #print(lst[i]) # 모든 행(리스트)
        for j in range(len(lst[i])):
            # print(lst[i][j])
            num += lst[i][j]
    # 이차원리스트 갯수 변수
    

    average_num = num / (len(lst) * len(lst[0]))
    # 평균을 결과로 반환
    # 숫자를 소수점 1번째자리까지 출력되게 하기 
    # round(데이터, 자리수) : 반올림 하는 함수 / 올림함수, 내일함수는 별로 따로 존재 => 다음시간에 맛보기
    return round(average_num, 1)

